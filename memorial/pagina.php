				<h2 class="title">Um memorial para a pandemia</h2>

				<p>O Memorial Digital da Pandemia de Covid-19 é uma iniciativa do Centro Cultural do Ministério da Saúde (CCMS) para preservar e dar acesso às memórias da pandemia no Brasil. Desde 11 de março de 2020, quando a Organização Mundial da Saúde decretou estado de pandemia por conta da propagação do Novo Coronavírus, pessoas em todo o mundo começaram a registrar as suas experiências de vida durante esse evento tão assustador e incerto. No Brasil não foi diferente: cidadãs e cidadãos brasileiros vêm registrando continuamente as suas memórias da pandemia. Preservar esses registros é fazer justiça a todas e todos que sofreram durante esse evento, dando continuidade à luta de muitas pessoas para que se construa uma memória e uma história da pandemia no Brasil baseada na ética, no respeito e na cidadania.</p>

				<h2 class="title">Um memorial digital</h2>

				<p>Ainda durante a pandemia, dezenas de iniciativas em todo o país começaram a reunir depoimentos, fotos, vídeos e outros registros sobre a pandemia, muitas vezes criadas de forma voluntária e criativa. Por conta da ampla difusão de tecnologias digitais, nós pudemos tirar fotos, postar em redes sociais e trocar, pela internet, informações e histórias da vida durante a pandemia, com familiares, amigos e até mesmo desconhecidos. No contexto de isolamento social, a escolha por registrar a vida usando ferramentas fazia ainda mais sentido. Foram milhares de documentos digitais que surgiram em todo o país, dando a impressão de que a memória da pandemia seria rica, democrática e duradoura. No entanto, documentos digitais podem ser considerados efêmeros, porque correm sempre o risco de se perder com o tempo, por conta de fatores que vivemos diariamente: computadores e smartphones quebram, senhas de servidores de nuvens são esquecidas, redes sociais são descontinuadas, HDs param de funcionar, ou mesmo podemos nos perder com quantidades gigantes de registros que deveríamos organizar. Para que os vídeos, as fotos e os demais documentos digitais que registram a pandemia possam ser preservados, é preciso cuidado, organização e infraestrutura. É isso que o Memorial Digital proporciona: um lugar seguro construído por especialistas dedicados a manter vivas as importantes memórias que construímos, no passado e atualmente, sobre a pandemia de </p>COVID-19.
