			<h2>Introduction</h2>
			<p>The objective of this document is to present available pharmaceutical and health technology information of the country of Guyana. The following is a summary of the methodology and results.</p>

			<h2>Methods</h2>
			<p>Pre-defined indicators were initially organized by the research team into 13 domains. Data for each of these indicators was gathered in two ways: (i) the Pan American Health Organization submitted data from the Regional Platform on Access and Innovation for Health Technologies (PRAIS). Some of the information in PRAIS is provided by governments to PAHO directly without being published in other publication. (ii) The research team searched publically available primary and secondary data sources to obtain information on each of the pre-defined indicators. Key primary data sources included World Health Organisation and World Bank websites. Search methodology was completed solely on the internet using English language search terms on Google. The PAHO’s pharmaceutical profile developed jointly with Guyana’s Ministry of Health was used as a reference tool to locate the most current data.</p>

			<h2>Results</h2>
			<p>Noteworthy data gaps were mainly identified in the Domains of 1) Market Authorization, Licensing and prescribing, 2) Pricing/ Price Control, Procurement and Reimbursement and 3) Consumption of Medicines. Missing indicator values in other Domains were recorded, but were fewer in comparison to those reported above. The data values for the Domain of Blood Services were outdated.</p>

