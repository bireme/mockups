						<h4>Web Accessibility Laws & Policies</h4>		
						<p>This page lists governmental policies related to web accessibility, although it is not a comprehensive or definitive listing. We welcome additions or corrections to these listings via the submission form.</p>
						<p>The information on this page is not legal advice. If you have questions about the applicability of the policies to specific situations, please consult legal authorities for the appropriate jurisdiction.</p>
